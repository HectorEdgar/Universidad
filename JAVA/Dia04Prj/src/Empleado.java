
public class Empleado extends Persona{
	private String sueldo;
	private String nss;
	
	public Empleado(){
		
	}

	public String getSueldo() {
		return sueldo;
	}

	public void setSueldo(String sueldo) {
		this.sueldo = sueldo;
	}

	public String getNss() {
		return nss;
	}

	public void setNss(String nss) {
		this.nss = nss;
	}

	
}
